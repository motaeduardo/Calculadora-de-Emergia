       private void personalizarBarraVerde() {
        alfacecrescendo.setUI(new BasicProgressBarUI() {
            @Override
            protected void paintDeterminate(Graphics g, JComponent c) {
                Insets b = alfacecrescendo.getInsets();
                int width = alfacecrescendo.getWidth() - (b.left + b.right);
                int height = alfacecrescendo.getHeight() - (b.top + b.bottom);
                int barWidth = (int) (width * alfacecrescendo.getPercentComplete());

                // Pintar fundo
                g.setColor(Color.LIGHT_GRAY);
                g.fillRect(b.left, b.top, width, height);

                // Pintar barra verde escuro
                g.setColor(new Color(0, 128, 0));
                g.fillRect(b.left, b.top, barWidth, height);

                // Borda da barra
                g.setColor(Color.DARK_GRAY);
                g.drawRect(b.left, b.top, width - 1, height - 1);

                // Texto centralizado
                String texto = alfacecrescendo.getString();
                if (alfacecrescendo.isStringPainted() && texto != null) {
                    FontMetrics fm = g.getFontMetrics();
                    int stringWidth = fm.stringWidth(texto);
                    int stringHeight = fm.getAscent();
                    g.setColor(Color.BLACK);
                    g.drawString(texto, (width - stringWidth) / 2 + b.left,
                                 (height + stringHeight) / 2 + b.top - 2);
                }
            }
        });
    }
    
   
   private void adicionarProgresso(int valor) {
    progressoAtual += valor;
  
    if (progressoAtual > LIMITE) {
        progressoAtual = LIMITE;
       
    }
   
    alfacecrescendo.setValue(progressoAtual);
   alfacecrescendo.setString(progressoAtual + "%");


  


    if (progressoAtual == LIMITE) {
        ImageIcon icon = new ImageIcon(getClass().getResource("/Imagens/Alface.jpeg"));
        JOptionPane.showMessageDialog(this, "Alface pronto para a salada!", 
                                  "Mensagem", JOptionPane.INFORMATION_MESSAGE, icon);
       progressoAtual = 0;
       alfacecrescendo.setValue(progressoAtual);
       alfacecrescendo.setString("0%");//zera a barra novamente
    }
   }
